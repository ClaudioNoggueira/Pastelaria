    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        PedidoBEAN pedBEAN = new PedidoBEAN();
        PedidoControl contPed = new PedidoControl();
        PastelBEAN pastelBEAN = new PastelBEAN();
        PastelControl contPast = new PastelControl();
        BebidaBEAN bebidaBEAN = new BebidaBEAN();
        BebidaControl contBeb = new BebidaControl();

        List<PastelBEAN> listaDePastel = new ArrayList<>();
        List<BebidaBEAN> listaDeBebida = new ArrayList<>();

        pedBEAN.setIdPedido((Integer) tblDados.getValueAt(linhaTabela, 0));

        pastelBEAN.setNome_pastel((String) tblDados.getValueAt(linhaTabela, 1));
        listaDePastel = contPast.consultarNome(pastelBEAN);
        pastelBEAN = listaDePastel.get(0);
        pedBEAN.setIdPastel(pastelBEAN.getIdPastel());

        pedBEAN.setQtdePastel((Integer) tblDados.getValueAt(linhaTabela, 2));

        bebidaBEAN.setNome_bebida((String) tblDados.getValueAt(linhaTabela, 3));
        listaDeBebida = contBeb.consultarNome(bebidaBEAN);
        bebidaBEAN = listaDeBebida.get(0);
        pedBEAN.setIdBebida(bebidaBEAN.getIdBebida());

        pedBEAN.setQtdeBebida((Integer) tblDados.getValueAt(linhaTabela, 4));

        Object[] opcoes = {"Sim", "N√£o"};
        int i = JOptionPane.showOptionDialog(
                null, "Deseja excluir esse pedido: '" + pedBEAN.getIdPedido() + "' ?",
                "Exclus√£o", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                null, opcoes, opcoes[0]);

        if (i == JOptionPane.YES_OPTION) {
            pedBEAN = contPed.excluir(pedBEAN);
            lblStatus.setText(pedBEAN.getStatus());
            visuGeral();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

