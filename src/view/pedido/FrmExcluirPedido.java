    public void visuDataHora() {
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        Calendar date = Calendar.getInstance();
        int hora = date.get(Calendar.HOUR_OF_DAY) - 1;
        int min = date.get(Calendar.MINUTE);
        lblDataHora.setText(hora + ":" + min + " - " + formatter.format(date.getTime()));
    }

    public void visuGeral() {
        PastelBEAN pastelBEAN = new PastelBEAN();
        PastelControl contPast = new PastelControl();
        BebidaBEAN bebidaBEAN = new BebidaBEAN();
        BebidaControl contBeb = new BebidaControl();

        PedidoControl cont = new PedidoControl();
        List<PedidoBEAN> listaDeDados = new ArrayList<>();
        listaDeDados = cont.listar();

        DefaultTableModel tbm = (DefaultTableModel) tblDados.getModel();
        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }
        int i = 0;
        for (PedidoBEAN pedBEAN : listaDeDados) {
            tbm.addRow(new String[1]);
            tblDados.setValueAt(pedBEAN.getIdPedido(), i, 0);

            pastelBEAN.setIdPastel(pedBEAN.getIdPastel());
            pastelBEAN = contPast.consultarID(pastelBEAN);
            tblDados.setValueAt(pastelBEAN.getNome_pastel(), i, 1);

            tblDados.setValueAt(pedBEAN.getQtdePastel(), i, 2);

            bebidaBEAN.setIdBebida(pedBEAN.getIdBebida());
            bebidaBEAN = contBeb.consultarID(bebidaBEAN);
            tblDados.setValueAt(bebidaBEAN.getNome_bebida(), i, 3);

            tblDados.setValueAt(pedBEAN.getQtdeBebida(), i, 4);
            i++;
        }
    }

