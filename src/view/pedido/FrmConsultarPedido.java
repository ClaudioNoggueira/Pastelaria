    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        PedidoBEAN pedBEAN = new PedidoBEAN();
        PedidoControl contPed = new PedidoControl();

        if (chkID.isSelected() == true) {
            try {
                pedBEAN.setIdPedido(Integer.parseInt(txtID.getText()));
                pedBEAN = contPed.consultarID(pedBEAN);
                visuResult(pedBEAN);
                lblStatus.setText(pedBEAN.getStatus());
            } catch (NumberFormatException e) {
                lblStatus.setText("Sem ou ID para fazer a consulta.");
                visuGeral();
            } catch (NullPointerException e) {
                lblStatus.setText("Sem ID para fazer a consulta.");
                visuGeral();
            }

        } else {
            lblStatus.setText("Sem ou ID para fazer a consulta.");
            visuGeral();
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

