    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        PastelBEAN pastelBEAN = new PastelBEAN();
        PastelControl contPast = new PastelControl();

        if (chkNome.isSelected() == true) {
            txtID.setText("");
            pastelBEAN.setNome_pastel(txtNome.getText());
            List<PastelBEAN> listaDeDados = new ArrayList<>();
            listaDeDados = contPast.consultarNome(pastelBEAN);
            lblStatus.setText(pastelBEAN.getStatus());
            visuLista(listaDeDados);
        } else if (chkID.isSelected() == true) {
            txtNome.setText("");
            pastelBEAN.setIdPastel(Integer.parseInt(txtID.getText()));
            pastelBEAN = contPast.consultarID(pastelBEAN);
            lblStatus.setText(pastelBEAN.getStatus());
            visuResult(pastelBEAN);
        } else {
            lblStatus.setText("Sem nome ou ID para fazer a consulta.");
            visuGeral();
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

