    public void visuDataHora() {
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        Calendar date = Calendar.getInstance();
        int hora = date.get(Calendar.HOUR_OF_DAY) - 1;
        int min = date.get(Calendar.MINUTE);
        lblDataHora.setText(hora + ":" + min + " - " + formatter.format(date.getTime()));
    }

    public void visuGeral() {
        PastelControl contPast = new PastelControl();
        List<PastelBEAN> listaDeDados = new ArrayList<>();

        listaDeDados = contPast.listar();
        DefaultTableModel tbm = (DefaultTableModel) tblDados.getModel();
        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }
        int i = 0;
        for (PastelBEAN pastel : listaDeDados) {
            tbm.addRow(new String[1]);
            tblDados.setValueAt(pastel.getIdPastel(), i, 0);
            tblDados.setValueAt(pastel.getNome_pastel(), i, 1);
            tblDados.setValueAt(pastel.getPreco(), i, 2);
            tblDados.setValueAt(pastel.getImg_pastel(), i, 3);
            tblDados.setValueAt(pastel.getDescricao(), i, 4);
            i++;
        }
    }

